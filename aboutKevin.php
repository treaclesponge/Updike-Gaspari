          </p>
          <p>
            Having moved so much in my childhood I got the bug to continue traveling as soon as my college days ended at Oregon State.  After I left college I got a job as a flight attendant for an airline in southern California.  In the years that followed I was able to travel all over the US and many countries around the world.  I loved meeting people from other places and seeing how they lived.  The airline I worked for was acquired by another airline and I ended up living in Chicago and going back to school to continue my college education.  I loved living in Chicago and was fascinated by the architecture and the wonderful neighborhoods that make up the diversity of Chicago.
          </p>
          <p>
            My sister and her family lived in Seattle by this time, as did most of my friends from Oregon State.  My long distance bills were astounding.  So in 1991 I decided to move to Seattle to be close to my family and friends.  I arrived in town and, not knowing anything about the real estate market in Seattle, I put money down on a condo in downtown Woodinville at a project called "Le Chardonnay Condominiums."  I thought I was really moving up in the world.
          </p>
          <p>
            Little did I know at the time that everything I  would ever want to do in Washington would likely center around downtown Seattle  and not downtown Woodinville. Luckily  real estate is not a tattoo. I was able  to cancel that purchase and get my earnest money back. I had heard of this cool new project being  built in Seattle's Central Area and I decided to wait for one of the Pine  Street Cottages to come for sale. This  cottage project was so extraordinary because it brought national attention to  the need for more urban density in large metropolitan areas and less sprawl in  the surrounding suburbs and it was done in a manner that was appealing to young  investors like myself. I was so proud to  own one of the 10 small cottages and to have made my first real estate  investment.
          </p>

          <p>
            My parents were not quite so thrilled  however. I remember their first visit  and the shock on their faces when they saw how small the cottages were and how  transitional the neighborhood was at the time. That was ten years ago. Now those  cottages go for nearly four times what I paid for one originally. I saw the importance of finding the right  location and what I would describe as the &ldquo;adorability factor&rdquo; when I purchased  that cottage and I would site that purchase as the start of my fascination  (others might say obsession) with real estate in Seattle.
          </p>

          <p>
            <img class="bioImage" src="/images/about_kevinThumb4.jpg" alt="Kevin Gaspari" />I decided to continue my college career at  Seattle University - it was so close to my cottage and it was time to go  back. I was lucky enough to attend  classes during the week and fly to Rome every weekend for work. (I was still working as a flight attendant.) &nbsp;Shortly before returning to college I spent  several months living in Florence, Italy and immersing myself in the language  and culture. That time, not to mention  thousands of dollars spent on hundreds of hours of private Italian lessons,  enabled me to become an official translator for the airline I was working for  and secured me regular weekend flights to Rome. I was able to live in a place I loved, attend classes during the week  near my home, and pay for it all by flying back and forth to Rome on the weekends. Life was good.
          </p>
